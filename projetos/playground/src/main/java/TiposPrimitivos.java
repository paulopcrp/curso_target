public class TiposPrimitivos {

    public static void main(String[] args) {

        int idade = 3;
        System.out.println(idade);
        float salario = 454646556;
        System.out.println(salario);
        char letra = 'P';
        System.out.println(letra);
        boolean casado = false;
        System.out.println(casado);

        int idade1 = (int)42;
        System.out.println(idade1);

        float salario1 = (float)45646;
        System.out.println(salario1);
        boolean casado2 = (boolean) true;
        System.out.println(casado2);

        Integer idade7 = new Integer(4);
        System.out.println(idade7);
        Float salario4 = new Float(787879789.8/8);
        System.out.println(salario4);
        Character letras = new Character('P');
        System.out.println(letras);
    }
}
